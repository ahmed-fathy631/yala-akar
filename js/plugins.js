$(document).ready(function(){
  $(".toggle-mode").click(function(){
	$("html, body").toggleClass("dark-mode"),
	$(".toggle-mode1").toggleClass("toggle-mode2"),
	$(".bg-darkk").toggleClass("bg-lightt"),
	$("a").toggleClass("aa"),
	$(".dropdown-menu").toggleClass("dropdown-menu2"),
	$(".text").toggleClass("text2"),
	$(".div").toggleClass("div2"),
	$(".footer").toggleClass("footer2");
});
});

